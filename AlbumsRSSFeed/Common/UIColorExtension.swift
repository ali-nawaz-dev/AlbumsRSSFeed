import Foundation
import UIKit

extension UIColor {
    
  @nonobjc class var black: UIColor {
    return UIColor(red: 17.0 / 255.0, green: 18.0 / 255.0, blue: 38.0 / 255.0, alpha: 1.0)
  }
    
  @nonobjc class var white: UIColor {
    return UIColor(white: 1.0, alpha: 1.0)
  }
    
  @nonobjc class var black75: UIColor {
    return UIColor(white: 0.0, alpha: 0.75)
  }
    
  @nonobjc class var gray: UIColor {
    return UIColor(red: 142.0 / 255.0, green: 142.0 / 255.0, blue: 147.0 / 255.0, alpha: 1.0)
  }
    
  @nonobjc class var blue: UIColor {
    return UIColor(red: 0.0, green: 122.0 / 255.0, blue: 1.0, alpha: 1.0)
  }
}
